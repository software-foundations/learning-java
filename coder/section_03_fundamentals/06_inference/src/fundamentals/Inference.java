package fundamentals;

public class Inference {

	public static void main(String[] args) {

		double a = 4.5;
		
		System.out.println(a);

		var b = 4.5;

		System.out.println(b);

		var c = "Text";

		System.out.println(c);

		c = "Another Text";

		System.out.println(c);

		double d;

		d = 123.65;

		System.out.println(d);

		var e = 12.1;

		System.out.println(e);

		e = 12;
		
		System.out.println(e);
	}
}